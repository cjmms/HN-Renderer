void PointLight::renderLightSource(glm::mat4 view, glm::mat4 projection, Shader &shader) 
{
    glm::mat4 model(1.0f);
    model = glm::translate(model, LightPos);
    model = glm::scale(model, glm::vec3(0.2f));

    glm::mat4 mvp = projection * view * model;

    shader.setMat4("mvp", mvp);

    glBindVertexArray(Light_VAO);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}




void PointLight::renderContainer(glm::mat4 view, glm::mat4 projection, Shader& shader) 
{
    glm::vec3 cubePositions[] = {
    glm::vec3(0.0f,  0.0f,  0.0f),
    glm::vec3(2.0f,  5.0f, -15.0f),
    glm::vec3(-1.5f, -2.2f, -2.5f),
    glm::vec3(-3.8f, -2.0f, -12.3f),
    glm::vec3(2.4f, -0.4f, -3.5f),
    glm::vec3(-1.7f,  3.0f, -7.5f),
    glm::vec3(1.3f, -2.0f, -2.5f),
    glm::vec3(1.5f,  2.0f, -2.5f),
    glm::vec3(1.5f,  0.2f, -1.5f),
    glm::vec3(-1.3f,  1.0f, -1.5f)
    };

    Texture diffuse_map("res/Textures/wood_container.png", PNG);
    Texture specular_map("res/Textures/steel_frame.png", PNG);

    diffuse_map.bindTexture(GL_TEXTURE0);
    specular_map.bindTexture(GL_TEXTURE1);

    shader.setInt("material.diffuse", 0);
    shader.setInt("material.specular", 1);
    shader.setFloat("material.shininess", 32.0f);

    shader.setVec3("light.LightPos", LightPos);
    shader.setVec3("light.ambient", glm::vec3(0.2f));
    shader.setVec3("light.diffuse", glm::vec3(0.6f));
    shader.setVec3("light.specular", glm::vec3(1.0f));

    glBindVertexArray(Cube_VAO);

    for (size_t i = 0; i < 10; i++)
    {
        glm::mat4 model(1.0f);
        model = glm::translate(model, cubePositions[i]);
        glm::mat4 mvp = projection * view * model;

        shader.setMat4("mvp", mvp);
        shader.setMat4("model", model);

        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
}

