void Color::render(glm::mat4 view, glm::mat4 projection) 
{

    glm::mat4 model(1.0f);
    glm::mat4 mvp = projection * view * model;

    glUniformMatrix4fv(location, 1, GL_FALSE, glm::value_ptr(mvp));

    glDrawArrays(GL_TRIANGLES, 0, 36);
}